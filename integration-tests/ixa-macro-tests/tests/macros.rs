    fn record_sum(
        context: &mut Context,
        records: Rc<RefCell<Vec<(f64, u32)>>>,
        arg1: u32,
        arg2: u32,
        arg3: u32,
    ) {
        records
            .borrow_mut()
            .push((context.get_current_time(), arg1 + arg2 + arg3));
    }

    fn record_current_time(context: &mut Context, records: Rc<RefCell<Vec<f64>>>) {
        records.borrow_mut().push(context.get_current_time());
    }

